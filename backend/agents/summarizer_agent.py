def _is_termination(message: dict) -> bool:
    """
    AutoGen termination function.
    The simulation ends when the Summarizer outputs the report header.
    """
    content = message.get("content") or ""
    return "### 📋 Clinical Analysis Report" in content


def create_summarizer_agent(extra_context: str = "") -> autogen.AssistantAgent:
    """Factory function — returns a configured Summarizer Agent instance."""
    msg = SUMMARIZER_SYSTEM_MESSAGE
    if extra_context:
        msg += f"\n\n{extra_context}"
    return autogen.AssistantAgent(
        name="Summarizer",
        system_message=msg,
        llm_config={"config_list": [get_llm_config()]},
        is_termination_msg=_is_termination,
    )
